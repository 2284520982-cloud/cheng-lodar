// ================= Secure Memory Functions =================
VOID SecureZeroMemoryManual(PVOID ptr, SIZE_T size) {
    if (!ptr || size == 0) return;
    volatile BYTE* p = (volatile BYTE*)ptr;
    while (size--) *p++ = 0;
}

VOID SecureZeroMemoryAPI(PVOID ptr, SIZE_T size) {
    if (S_RtlSecureZeroMemory) {
        S_RtlSecureZeroMemory(ptr, size);
    } else {
        SecureZeroMemoryManual(ptr, size);
    }
}

